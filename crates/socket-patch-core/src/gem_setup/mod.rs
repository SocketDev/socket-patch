    #[cfg(unix)]
    #[tokio::test]
    async fn test_remove_surfaces_removal_failure_as_error() {
        // Deleting a file requires write permission on its directory. With the
        // plugin dir read-only, the removes fail — that failure must surface as
        // `Error`, not be swallowed into a false "removed" success while the
        // files are in fact still there.
        use std::os::unix::fs::PermissionsExt;

        let dir = tempfile::tempdir().unwrap();
        let root = dir.path();
        add_plugin_files(root, false).await;
        fs::set_permissions(&plugin_dir(root), std::fs::Permissions::from_mode(0o555))
            .await
            .unwrap();

        let r = remove_plugin_files(root, false).await;

        // Restore so the tempdir can be cleaned up regardless of the outcome.
        fs::set_permissions(&plugin_dir(root), std::fs::Permissions::from_mode(0o755))
            .await
            .unwrap();

        assert!(
            plugin_files_present(root).await,
            "the read-only dir means nothing was actually removed"
        );
        assert_eq!(
            r.status,
            GemSetupStatus::Error,
            "a failed removal must report Error, not a false success"
        );
        assert!(
            r.error.is_some(),
            "the failure carries the io error message"
        );
    }

