//! Gem (Bundler) `setup` support: wire a Ruby project for automatic patching.
//!
//! Bundler has no after-each-install hook that survives a cached/no-op
//! `bundle install`, but it loads any declared **plugin** during the Gemfile
//! pass on every `bundle` invocation. So setup delivers the gate as a
//! generated, git-committed Bundler plugin plus a `plugin` directive in the
//! Gemfile:
//!
//!   * `.socket/bundler-plugin/{plugins.rb, socket-patch.gemspec}` — a generated
//!     plugin whose `plugins.rb` re-runs `socket-patch apply --ecosystems gem`
//!     on every `bundle install` (load-time digest gate + `after-install-all`
//!     hook), failing the build loudly on a patch failure;
//!   * a managed block appended to the `Gemfile` that references the plugin via
//!     `plugin "socket-patch", path: File.expand_path(".socket/bundler-plugin",
//!     __dir__)`. The source must be `path:` — Bundler fetches `git:` plugin
//!     sources with `git clone`, and the generated dir is a plain directory
//!     (committing it to the parent repo does not give it a `.git`), so `git:`
//!     fails every `bundle install`. The directory still must be committed so
//!     clones and CI have the plugin on disk.
//!
//! The actual gem patching is done by `apply` (unchanged); this module only
//! manages the setup wiring. Phase 2 (follow-up) replaces the in-tree plugin
//! with a published `socket-patch-bundler` gem.

mod update;

use std::path::{Path, PathBuf};

use tokio::fs;

pub use update::{
    add_plugin_directive, is_plugin_directive_present, remove_plugin_directive, GemEditResult,
    GemSetupStatus,
};

/// The in-tree plugin directory, relative to the project root.
const PLUGIN_DIR: &str = ".socket/bundler-plugin";
/// First line of every generated plugin file — the ownership signal for removal
/// (we never delete a file that lacks it).
const GENERATED_MARKER: &str = "# Code generated by `socket-patch setup`. DO NOT EDIT.";

/// The generated `plugins.rb` body (the two-trigger idempotent applier).
const PLUGINS_RB: &str = include_str!("templates/plugins.rb.tmpl");
/// The generated plugin gemspec.
const GEMSPEC: &str = include_str!("templates/gemspec.tmpl");

/// A discovered Bundler project.
#[derive(Debug, Clone)]
pub struct BundlerProject {
    /// Directory containing the Gemfile (the project root). The plugin dir and
    /// `.socket/manifest.json` live here.
    pub root: PathBuf,
    /// The Bundler manifest to edit (`Gemfile` or `gems.rb`).
    pub gemfile: PathBuf,
}

/// Find the Bundler project that `cwd` belongs to by walking up to the nearest
/// directory holding a `Gemfile` (or Bundler's alternate `gems.rb`) — exactly
/// how `bundle` itself resolves the manifest. The discovered
/// directory (not `cwd`) becomes the project `root`, so `.socket/` and the
/// plugin dir land next to the Gemfile even when `setup` is run from a
/// subdirectory. Returns `None` when no ancestor has one — a `Gemfile.lock`
/// alone is not editable, so it does not count.
pub async fn discover_bundler_project(cwd: &Path) -> Option<BundlerProject> {
    let mut dir = cwd.to_path_buf();
    loop {
        for name in ["Gemfile", "gems.rb"] {
            let candidate = dir.join(name);
            if fs::metadata(&candidate).await.is_ok() {
                return Some(BundlerProject {
                    root: dir,
                    gemfile: candidate,
                });
            }
        }
        dir = if matches!(
            dir.components().next_back(),
            Some(std::path::Component::ParentDir)
        ) {
            // `Path::parent` is lexical: it strips a trailing `..`, stepping
            // the walk back DOWN into the directory the `..` just escaped
            // (parent of `a/b/..` is `a/b`) and probing descendants outside
            // the real ancestry. Resolve the position against the filesystem —
            // the same way the kernel resolved this iteration's metadata
            // probes — and continue from its real parent.
            fs::canonicalize(&dir).await.ok()?.parent()?.to_path_buf()
        } else {
            match dir.parent() {
                Some(parent) if !parent.as_os_str().is_empty() => parent.to_path_buf(),
                // A relative `cwd` (e.g. the CLI's default `--cwd .`) exhausts its
                // lexical components here (`Path::parent` of `.` is `Some("")`,
                // then `None`) without ever reaching the real parent directories.
                // Re-root the walk on the process cwd so the true ancestry is
                // still probed — `bundle` resolves the Gemfile from the invocation
                // dir's real ancestors, wherever it is run from.
                _ if dir.is_relative() => std::env::current_dir()
                    .ok()?
                    .join(&dir)
                    .parent()?
                    .to_path_buf(),
                _ => return None,
            }
        };
    }
}

/// Absolute path to the generated plugin directory for a project root.
pub fn plugin_dir(root: &Path) -> PathBuf {
    root.join(PLUGIN_DIR)
}

fn plugins_rb_path(root: &Path) -> PathBuf {
    plugin_dir(root).join("plugins.rb")
}

fn gemspec_path(root: &Path) -> PathBuf {
    plugin_dir(root).join("socket-patch.gemspec")
}

/// Whether the generated plugin files are present (the `setup --check`
/// "configured" signal, paired with the Gemfile directive check).
pub async fn plugin_files_present(root: &Path) -> bool {
    fs::metadata(plugins_rb_path(root)).await.is_ok()
        && fs::metadata(gemspec_path(root)).await.is_ok()
}

/// True if the file is absent or its content differs from `desired`.
async fn needs_write(path: &Path, desired: &str) -> bool {
    match fs::read_to_string(path).await {
        Ok(c) => c != desired,
        Err(_) => true,
    }
}

async fn write_file(path: &Path, body: &str) -> Result<(), String> {
    if let Some(p) = path.parent() {
        fs::create_dir_all(p)
            .await
            .map_err(|e| format!("create {}: {e}", p.display()))?;
    }
    fs::write(path, body)
        .await
        .map_err(|e| format!("write {}: {e}", path.display()))
}

/// Generate `.socket/bundler-plugin/{plugins.rb, socket-patch.gemspec}`.
/// Idempotent: `AlreadyConfigured` when both already match the templates byte
/// for byte. `kind = "gem_plugin"`.
async fn add_plugin_files(root: &Path, dry_run: bool) -> GemEditResult {
    let dir = plugin_dir(root);
    let result = async {
        let rb_changed = needs_write(&plugins_rb_path(root), PLUGINS_RB).await;
        let spec_changed = needs_write(&gemspec_path(root), GEMSPEC).await;
        if !rb_changed && !spec_changed {
            return Ok(false);
        }
        if !dry_run {
            if rb_changed {
                write_file(&plugins_rb_path(root), PLUGINS_RB).await?;
            }
            if spec_changed {
                write_file(&gemspec_path(root), GEMSPEC).await?;
            }
        }
        Ok(true)
    }
    .await;
    GemEditResult::from_result("gem_plugin", dir.display().to_string(), result)
}

/// Whether the file at `path` carries our [`GENERATED_MARKER`] as its first
/// line — the per-file ownership test for removal.
async fn is_generated(path: &Path) -> bool {
    match fs::read_to_string(path).await {
        Ok(content) => content.starts_with(GENERATED_MARKER),
        Err(_) => false,
    }
}

/// Delete a generated file, tolerating it already being gone but surfacing
/// any other failure (a swallowed error here would report a false "removed").
async fn remove_generated(path: &Path) -> Result<(), String> {
    match fs::remove_file(path).await {
        Ok(()) => Ok(()),
        Err(e) if e.kind() == std::io::ErrorKind::NotFound => Ok(()),
        Err(e) => Err(format!("remove {}: {e}", path.display())),
    }
}

/// Remove the generated plugin files — each only when it carries our
/// [`GENERATED_MARKER`], so a user-authored file at either path is never
/// deleted (and an orphaned generated file is still cleaned up). Idempotent:
/// `AlreadyConfigured` when nothing of ours is there.
async fn remove_plugin_files(root: &Path, dry_run: bool) -> GemEditResult {
    let dir = plugin_dir(root);
    let result = async {
        let rb_ours = is_generated(&plugins_rb_path(root)).await;
        let spec_ours = is_generated(&gemspec_path(root)).await;
        if !rb_ours && !spec_ours {
            return Ok(false);
        }
        if !dry_run {
            if rb_ours {
                remove_generated(&plugins_rb_path(root)).await?;
            }
            if spec_ours {
                remove_generated(&gemspec_path(root)).await?;
            }
            // Prune the now-empty plugin dir (leave .socket/ — apply uses it).
            let _ = fs::remove_dir(&dir).await;
        }
        Ok(true)
    }
    .await;
    GemEditResult::from_result("gem_plugin", dir.display().to_string(), result)
}

