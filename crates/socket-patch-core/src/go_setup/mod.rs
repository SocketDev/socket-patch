    #[tokio::test]
    async fn test_find_main_dirs_and_imports() {
        let dir = tempfile::tempdir().unwrap();
        let root = dir.path();
        write(&root.join("go.mod"), "module example.com/app\n\ngo 1.21\n").await;
        // A main package at cmd/server.
        write(&root.join("cmd/server/main.go"), "package main\n\nfunc main() {}\n").await;
        // A library package (not main) — must be ignored.
        write(&root.join("pkg/lib/lib.go"), "package lib\n").await;
        // A main package at root.
        write(&root.join("main.go"), "package main\n\nfunc main() {}\n").await;
        // vendored main — must be ignored.
        write(&root.join("vendor/x/cmd/main.go"), "package main\n\nfunc main() {}\n").await;

        let dirs = find_main_package_dirs(root).await;
        assert!(dirs.contains(&root.to_path_buf()));
        assert!(dirs.contains(&root.join("cmd/server")));
        assert!(!dirs.iter().any(|d| d.starts_with(root.join("vendor"))));
        assert!(!dirs.contains(&root.join("pkg/lib")));

        // Add imports → one per main dir.
        let added = add_main_imports(root, "example.com/app", false).await;
        assert_eq!(added.len(), 2);
        assert!(added.iter().all(|r| r.status == GoSetupStatus::Updated));
        let import_body = fs::read_to_string(import_file_path(&root.join("cmd/server")))
            .await
            .unwrap();
        assert!(import_body.contains("import _ \"example.com/app/internal/socketpatchguard\""));

        // Idempotent.
        assert!(add_main_imports(root, "example.com/app", false)
            .await
            .iter()
            .all(|r| r.status == GoSetupStatus::AlreadyConfigured));

        // Remove only marker-bearing files.
        let removed = remove_main_imports(root, false).await;
        assert_eq!(removed.len(), 2);
        assert!(!import_file_path(&root.join("cmd/server")).exists());
    }

